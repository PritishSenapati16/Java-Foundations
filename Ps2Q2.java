// Signing in
public class Ps2Q2 {
    public static void main(String[] args) {
        char grade = 'B';
        grade = (char)(grade + 8);
        System.out.println(grade);

        // Decrypting the grade
        grade = (char)(grade - 8);
        System.out.println(grade);
        System.out.println("END OF THE PROGRAM");
    }
}
// Signing off