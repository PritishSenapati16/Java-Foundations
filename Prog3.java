// Signing in
public class Prog3 {
    public static void main(String[] args) {
        byte age = 52;
        int age1 = 36;
        long age2 = 5676547L;
        char ch = 'P';
        float f1 = 3.6f;
        double d1 = 3.66d;
        System.out.println(age);
        System.out.println(age1);
        System.out.println(age2);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
    }
}
// Signing off